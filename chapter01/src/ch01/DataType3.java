package ch01;

public class DataType3 {
	
	// 코드의 시작점
	public static void main(String[] args) {
		// 기본 자료형 (값), 참조 자료형 (주소값)
		
	
//		  실수형 2가지 타입
//		  1. float
//		 2. double
		 
		
		// float
		float a = 0.5F;	// 접미사 (대소문자 가능하나, 대문자 권장)
		float b = 0.1f;	// 4바이트
		
		// double
		double c = 0.12345;		// 실수형 기본값 double
		double d = 0.555;		// 8바이트
		// 소수점을 많이 표시할 수 있기 때문에 정확성을 더 보장한다.
		
		
	} // end of main

} // end of class
